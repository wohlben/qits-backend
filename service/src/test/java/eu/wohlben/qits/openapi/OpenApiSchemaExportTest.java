package eu.wohlben.qits.openapi;

import io.quarkus.test.junit.QuarkusTest;
import org.junit.jupiter.api.Test;

import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;

import static io.restassured.RestAssured.given;

@QuarkusTest
public class OpenApiSchemaExportTest {

    @Test
    public void exportOpenApiSchema() throws Exception {
        String schema = given()
            .when()
            .get("/q/openapi")
            .then()
            .statusCode(200)
            .extract()
            .asString();

        Path docsDir = Paths.get("../docs");
        if (!Files.exists(docsDir)) {
            Files.createDirectories(docsDir);
        }

        String header = "# THIS FILE IS GENERATED BY OpenApiSchemaExportTest — DO NOT EDIT MANUALLY.\n" +
                        "# Run `mvnw test -Dtest=OpenApiSchemaExportTest` in the service module to regenerate.\n";

        Path target = docsDir.resolve("openapi.yml");
        Files.writeString(target, header + schema);
    }
}
